/*  
    Los operadores aritmeticos permiten realizar operaciones matematicas
*/

let a = 5;
let b = 10;

console.log(a + b); //adicion
console.log(a - b); //resta
console.log(a * b); //multiplicacion
console.log(a / b); //division
console.log(a % b); //modulo
console.log(a ** b); //potencia


//Incrementar el valor de a en 1 (5=6)
console.log(++a);  
console.log (a++)


//Decrementar el valor de b en 1 (10=9)
console.log(--b);
console.log(b--);